    private void txtBuscarNombreKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarNombreKeyReleased
        if (txtBuscarNombre.getText().equals("")) {
            borraLista();
        } else {
            borraLista();
            actualizaLista();
        }
    }//GEN-LAST:event_txtBuscarNombreKeyReleased

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if (txtBuscarNombre.getText().equals("")) {
            borraLista();
        } else {
            borraLista();
            actualizaLista();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
        int fila = 0;
        fila = jTable1.getSelectedRow();
        copiar = jTable1.getValueAt(fila, 0).toString();
        nombre = jTable1.getValueAt(fila, 1).toString();
        aPat = jTable1.getValueAt(fila, 2).toString();
        aMat = jTable1.getValueAt(fila, 3).toString();
        Registrar.txtIdEmpleado.setText(copiar);
        Registrar.txtNombre.setText(nombre+" "+aPat+" "+aMat);
        System.exit(0);
    }//GEN-LAST:event_btnSeleccionarActionPerformed

