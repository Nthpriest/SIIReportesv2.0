    public void actualizaLista() {
        emjc = new EmpleadosJpaController();
        String nombre = txtBuscarNombre.getText().trim();
        List<Empleado> lista = emjc.getEmpleadosPorNombreLike(nombre);
        for (Empleado empleado : lista) {
            tabla.addRow(new Object[]{empleado.getIdEmpleados(),
                empleado.getNombre(), empleado.getAPaterno(),
                empleado.getAMaterno()});
        }
    }

    public void borraLista() {
        for (int i = 0; i < tabla.getRowCount(); i++) {
            tabla.removeRow(i);
            i--;
        }
    }

